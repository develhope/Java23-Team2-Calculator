public class Main {

    public static void main(String[] args) {

        System.out.println(sum(2, 4));
        System.out.println(multiplication(2, 10));
    }


    public static int sum(int a, int b) {
        return a + b;

    }

    public static int multiplication(int a, int b) {
        return a * b;
    }
}
